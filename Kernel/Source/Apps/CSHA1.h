#ifndef _CSHA1_H
#define _CSHA1_H
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//This class implements the Secure Hashing Standard as defined
//	in FIPS PUB 180-1 published April 17, 1995.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct {
    uint state[5];
    uint count[2];
    unsigned char buffer[64];
} SHA1_CTX;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void SHA1Transform(uint state[5], unsigned char buffer[64]);
void SHA1Init(SHA1_CTX* context);
void SHA1Update(SHA1_CTX* context, unsigned char* data, uint len);
void SHA1Final(unsigned char digest[20], SHA1_CTX* context);
void SimpleSHA1(unsigned char *sInput, int iInputLength, unsigned char *sOutHash);
void ToString(unsigned char *sDigest, unsigned char *sOut);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#endif
